package com.upc.dto;

public class TipoFondo {

	private int idTF;
	private String nombreTF;
	private String detalleTF;
	
	public int getIdTF() {
		return idTF;
	}
	public void setIdTF(int idTF) {
		this.idTF = idTF;
	}
	public String getNombreTF() {
		return nombreTF;
	}
	public void setNombreTF(String nombreTF) {
		this.nombreTF = nombreTF;
	}
	public String getDetalleTF() {
		return detalleTF;
	}
	public void setDetalleTF(String detalleTF) {
		this.detalleTF = detalleTF;
	}
	
}
