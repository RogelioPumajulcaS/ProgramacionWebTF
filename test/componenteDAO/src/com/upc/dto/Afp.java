package com.upc.dto;

public class Afp {
	
	private int idAfp;
	private String nombreAfp;
	private String descripcionAfp;
	
	public int getIdAfp() {
		return idAfp;
	}
	public void setIdAfp(int idAfp) {
		this.idAfp = idAfp;
	}
	public String getNombreAfp() {
		return nombreAfp;
	}
	public void setNombreAfp(String nombreAfp) {
		this.nombreAfp = nombreAfp;
	}
	public String getDescripcionAfp() {
		return descripcionAfp;
	}
	public void setDescripcionAfp(String descripcionAfp) {
		this.descripcionAfp = descripcionAfp;
	}
	
}
